namespace app\manage\controller;


use app\common\controller\Manage;

class Withdraw extends Manage
{

    public function _initialize()
    {
        parent::_initialize(); // TODO: Change the autogenerated stub
        $this->table = "Withdraw";
        $this->model = model($this->table);
        $this->join = [
            ['business b','b.id = a.bus_id','left']
        ];
        $this->field = "a.*,b.name";
//        $this->admin_id = session('userInfo')->id;
    }

    /**
     *  提款审核
     * @return mixed
     * @throws \think\Exception
     */
    public function check_status()
    {
        $id = $this->request->param('id', 0, 'intval');
        if (empty($id)) {
            $this->error('参数错误');
        }
        $where = [
            'id' => $id,
            'delete_time' => 0
        ];
        $field = 'id,status,check_desc,check_time,money';
        $data = $this->model->where($where)->field($field)->find();
        if ($data->status !== 0) {
            $this->error('该申请以审核,请勿重复申请');
        }
        if (!$data) {
            $this->error('该申请不存在');
        }
        $bus_name = db('business')->where('id', $id)->value('name');
        if (request()->post()) {
            $post_data = $this->request->param('');
            if (!isset($post_data['status'])) {
                $this->error('请选择状态');
            }
            $post_data['check_time'] = time();
            $result = $this->model->allowField(true)->save($post_data, ['id' => $id]);
            if ($result) {
                \app\manage\model\Withdraw::addWithdrawLog($post_data);
                operaLog($this->admin_id . '对商户(' . $bus_name . ')提款审核');
                $this->success("设置成功！");
            }
            $this->error("请稍后再试！");
        }
        $data['check_time'] = date('Y-m-d H:i:s', $data['check_time']);
        $this->assign('data', $data);
        return $this->fetch();
    }

}